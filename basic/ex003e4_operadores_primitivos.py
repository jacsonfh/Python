""" Os principais tipos pritivos são:
int = número inteiro.
float = número de ponto flutuante ou reais.
bool = expressao boleana.
str = texto """
qqc = input('Digite quaquer coisa:')
print('O tipo primitivo é:', type(qqc))
print(qqc.isalpha(), 'é Alpha.')
print(qqc.isspace(), 'é Espaço.')
print(qqc.isupper(), 'é maiúsculo.')
print(qqc.islower(), 'é minúsculo.')
print(qqc.isnumeric(), 'é número.')
print(qqc.istitle()), 'A primeira letra é maiúscula.'
